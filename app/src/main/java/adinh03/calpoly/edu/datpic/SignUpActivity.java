package adinh03.calpoly.edu.datpic;

import android.app.AlertDialog;
import android.content.ContentResolver;
import android.content.Intent;
import android.content.res.Resources;
import android.net.Uri;
import android.os.Bundle;
import android.support.annotation.NonNull;
import android.support.v7.app.AppCompatActivity;
import android.view.View;
import android.widget.Button;
import android.widget.EditText;

import com.google.android.gms.tasks.OnCompleteListener;
import com.google.android.gms.tasks.Task;
import com.google.firebase.auth.AuthResult;

import com.google.firebase.auth.FirebaseAuth;
import com.google.firebase.storage.FirebaseStorage;

/**
 * Created by tjyung on 10/28/16.
 */
public class SignUpActivity extends AppCompatActivity
{

   protected EditText passwordEditText;
   protected EditText emailEditText;
   protected Button signUpButton;
   private FirebaseAuth mFirebaseAuth;
   private FirebaseStorage mStorage;
   private static String nName;
   protected EditText nicknameEditText;

   @Override
   protected void onCreate(Bundle savedInstanceState)
   {
      super.onCreate(savedInstanceState);
      setContentView(R.layout.activity_sign_up);

      // Initialize FirebaseAuth
      mFirebaseAuth = FirebaseAuth.getInstance();
      mStorage = FirebaseStorage.getInstance();

      passwordEditText = (EditText) findViewById(R.id.passwordField);
      emailEditText = (EditText) findViewById(R.id.emailField);
      signUpButton = (Button) findViewById(R.id.signupButton);

      nicknameEditText = (EditText) findViewById(R.id.nicknameField);

      signUpButton.setOnClickListener(new View.OnClickListener()
      {
         @Override
         public void onClick(View v)
         {
            String password = passwordEditText.getText().toString();
            String email = emailEditText.getText().toString();
            String nickname = nicknameEditText.getText().toString();

            password = password.trim();
            email = email.trim();
            nickname = nickname.trim();
            nName = nickname;

            if (password.isEmpty() || email.isEmpty() || nickname.isEmpty())
            {
               AlertDialog.Builder builder = new AlertDialog.Builder(SignUpActivity.this);
               builder.setMessage(R.string.signup_error_message)
                     .setTitle(R.string.signup_error_title)
                     .setPositiveButton(android.R.string.ok, null);
               AlertDialog dialog = builder.create();
               dialog.show();
            }
            else
            {
               mFirebaseAuth.createUserWithEmailAndPassword(email, password)
                     .addOnCompleteListener(SignUpActivity.this, new
                           OnCompleteListener<AuthResult>()
                           {
                              @Override
                              public void onComplete(@NonNull Task<AuthResult> task)
                              {
                                 if (task.isSuccessful())
                                 {
                                    Intent intent = new Intent(SignUpActivity.this, MainActivity
                                          .class);
                                    intent.addFlags(Intent.FLAG_ACTIVITY_NEW_TASK);
                                    intent.addFlags(Intent.FLAG_ACTIVITY_CLEAR_TASK);
                                    intent.putExtra("nickname", nName);
                                    Resources res = getResources();
                                    Uri resUri = Uri.parse(ContentResolver.SCHEME_ANDROID_RESOURCE +
                                          "://" + res.getResourcePackageName(R.mipmap.ic_launcher)
                                          + '/' + res.getResourceTypeName(R.mipmap.ic_launcher)
                                          + '/' + res.getResourceEntryName(R.mipmap.ic_launcher));
                                    mStorage.getReference().child("Photos").child(task.getResult
                                          ().getUser().getUid()).child("ProfilePicture").putFile
                                          (resUri);
                                    startActivity(intent);
                                 }
                                 else
                                 {
                                    AlertDialog.Builder builder = new AlertDialog.Builder
                                          (SignUpActivity.this);
                                    builder.setMessage(task.getException().getMessage())
                                          .setTitle(R.string.login_error_title)
                                          .setPositiveButton(android.R.string.ok, null);
                                    AlertDialog dialog = builder.create();
                                    dialog.show();
                                 }
                              }
                           });
            }
         }
      });
   }
}
